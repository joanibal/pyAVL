        cdtot = 2.0*cdtot
        cltot_diff = 2.0*cltot_diff
        cltot = 2.0*cltot
        cxtot_diff = 2.0*cxtot_diff
        cxtot = 2.0*cxtot
        cytot = 0.
        cztot_diff = 2.0*cztot_diff
        cztot = 2.0*cztot
        crtot = 0.
        cmtot_diff = 2.0*cmtot_diff
        cmtot = 2.0*cmtot
        cntot = 0.
        cdvtot_diff = 2.0*cdvtot_diff
        cdvtot = 2.0*cdvtot
C
        cdtot_a = 2.0*cdtot_a
        cltot_a = 2.0*cltot_a
C
        DO n=1,numax
          cdtot_u(n) = 2.0*cdtot_u(n)
          cltot_u(n) = 2.0*cltot_u(n)
          cxtot_u(n) = 2.0*cxtot_u(n)
          cytot_u(n) = 0.
          cztot_u(n) = 2.0*cztot_u(n)
          crtot_u(n) = 0.
          cmtot_u(n) = 2.0*cmtot_u(n)
          cntot_u(n) = 0.
        ENDDO
C
        DO n=1,ncontrol
          cdtot_d_diff(n) = 2.0*cdtot_d_diff(n)
          cdtot_d(n) = 2.0*cdtot_d(n)
          cltot_d_diff(n) = 2.0*cltot_d_diff(n)
          cltot_d(n) = 2.0*cltot_d(n)
          cxtot_d_diff(n) = 2.0*cxtot_d_diff(n)
          cxtot_d(n) = 2.0*cxtot_d(n)
          cytot_d_diff(n) = 0.D0
          cytot_d(n) = 0.
          cztot_d_diff(n) = 2.0*cztot_d_diff(n)
          cztot_d(n) = 2.0*cztot_d(n)
          crtot_d_diff(n) = 0.D0
          crtot_d(n) = 0.
          cmtot_d_diff(n) = 2.0*cmtot_d_diff(n)
          cmtot_d(n) = 2.0*cmtot_d(n)
          cntot_d_diff(n) = 0.D0
          cntot_d(n) = 0.
        ENDDO
C
        DO n=1,ndesign
          cdtot_g(n) = 2.0*cdtot_g(n)
          cltot_g(n) = 2.0*cltot_g(n)
          cxtot_g(n) = 2.0*cxtot_g(n)
          cytot_g(n) = 0.
          cztot_g(n) = 2.0*cztot_g(n)
          crtot_g(n) = 0.
          cmtot_g(n) = 2.0*cmtot_g(n)
          cntot_g(n) = 0.
        ENDDO
        cytot_diff = 0.D0
        crtot_diff = 0.D0
        cntot_diff = 0.D0
      END IF
C
      RETURN
      END

C  Differentiation of bdforc in forward (tangent) mode (with options i4 dr8 r8):
C   variations   of useful results: cdtot cltot cxtot cytot cztot
C                crtot cmtot cntot
C   with respect to varying inputs: alfa vinf sref xyzref mach
C                cdtot cltot cxtot cytot cztot crtot cmtot cntot
C SFFORC
C
C
C
      SUBROUTINE BDFORC_D()
      INCLUDE 'AVL.INC'
      INCLUDE 'AVL_ad_seeds.inc'
C
      REAL rrot(3)
      REAL rrot_diff(3)
      REAL veff(3), vrot(3), veff_u(3, 6), vrot_u(3), wrot_u(3)
      REAL veff_diff(3), vrot_diff(3)
      REAL drl(3), esl(3), fb(3), fb_u(3, numax), mb(3), mb_u(3, numax)
      REAL drl_diff(3), esl_diff(3), fb_diff(3), mb_diff(3)
      REAL cdbdy_u(numax), clbdy_u(numax), cxbdy_u(numax), cybdy_u(numax
     +     ), czbdy_u(numax), crbdy_u(numax), cmbdy_u(numax), cnbdy_u(
     +     numax)
      REAL betm
      REAL betm_diff
      INTRINSIC SQRT
      REAL sina
      REAL sina_diff
      INTRINSIC SIN
      REAL cosa
      REAL cosa_diff
      INTRINSIC COS
      INTEGER ib
      INTEGER iu
      INTEGER ilseg
      INTEGER l1
      INTEGER l2
      INTEGER l
      REAL drlmag
      REAL drlmag_diff
      REAL drlmi
      REAL drlmi_diff
      REAL dia
      REAL dinv
      INTEGER k
      REAL us
      REAL us_diff
      REAL un
      REAL un_diff
      REAL un_u
      REAL(kind=8) arg1
      REAL(kind=8) arg1_diff
      REAL arg10
      REAL arg10_diff
      REAL(kind=avl_real) temp
      REAL(kind=8) temp0
      REAL temp1
      INTEGER ii1
C
C
      arg1_diff = -(2*mach*mach_diff)
      arg1 = 1.0 - mach**2
      temp = SQRT(arg1)
      IF (arg1 .EQ. 0.D0) THEN
        betm_diff = 0.D0
      ELSE
        betm_diff = arg1_diff/(2.0*temp)
      END IF
      betm = temp
C
      sina_diff = COS(alfa)*alfa_diff
      sina = SIN(alfa)
      cosa_diff = -(SIN(alfa)*alfa_diff)
      cosa = COS(alfa)
      DO ii1=1,nbmax
        cdbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        clbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        cxbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        cybdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        czbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        crbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        cnbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,nbmax
        cmbdy_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        esl_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        drl_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        vrot_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        fb_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        veff_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        mb_diff(ii1) = 0.D0
      ENDDO
      DO ii1=1,3
        rrot_diff(ii1) = 0.D0
      ENDDO
C
C
C---- add on body force contributions
      DO ib=1,nbody
        cdbdy_diff(ib) = 0.D0
        cdbdy(ib) = 0.0
        clbdy_diff(ib) = 0.D0
        clbdy(ib) = 0.0
        cxbdy_diff(ib) = 0.D0
        cxbdy(ib) = 0.0
        cybdy_diff(ib) = 0.D0
        cybdy(ib) = 0.0
        czbdy_diff(ib) = 0.D0
        czbdy(ib) = 0.0
        crbdy_diff(ib) = 0.D0
        crbdy(ib) = 0.0
        cmbdy_diff(ib) = 0.D0
        cmbdy(ib) = 0.0
        cnbdy_diff(ib) = 0.D0
        cnbdy(ib) = 0.0
C
        DO iu=1,6
          cdbdy_u(iu) = 0.0
          clbdy_u(iu) = 0.0
          cxbdy_u(iu) = 0.0
          cybdy_u(iu) = 0.0
          czbdy_u(iu) = 0.0
          crbdy_u(iu) = 0.0
          cmbdy_u(iu) = 0.0
          cnbdy_u(iu) = 0.0
        ENDDO
C
        DO ilseg=1,nl(ib)-1
          l1 = lfrst(ib) + ilseg - 1
          l2 = lfrst(ib) + ilseg
C
          l = l1
C
          temp0 = (rl(1, l2)-rl(1, l1))/betm
          drl_diff(1) = -(temp0*betm_diff/betm)
          drl(1) = temp0
          drl_diff(2) = 0.D0
          drl(2) = rl(2, l2) - rl(2, l1)
          drl_diff(3) = 0.D0
          drl(3) = rl(3, l2) - rl(3, l1)
          arg10_diff = 2*drl(1)*drl_diff(1) + 2*drl(2)*drl_diff(2) + 2*
     +      drl(3)*drl_diff(3)
          arg10 = drl(1)**2 + drl(2)**2 + drl(3)**2
          temp1 = SQRT(arg10)
          IF (arg10 .EQ. 0.D0) THEN
            drlmag_diff = 0.D0
          ELSE
            drlmag_diff = arg10_diff/(2.0*temp1)
          END IF
          drlmag = temp1
          IF (drlmag .EQ. 0.0) THEN
            drlmi = 0.0
            drlmi_diff = 0.D0
          ELSE
            drlmi_diff = -(drlmag_diff/drlmag**2)
            drlmi = 1.0/drlmag
          END IF
C
          dia = radl(l1) + radl(l2)
          IF (dia .LE. 0.0) THEN
            dinv = 0.0
          ELSE
            dinv = 1.0/dia
          END IF
C
C-------- unit vector along line segment
          esl_diff(1) = drlmi*drl_diff(1) + drl(1)*drlmi_diff
          esl(1) = drl(1)*drlmi
          esl_diff(2) = drlmi*drl_diff(2) + drl(2)*drlmi_diff
          esl(2) = drl(2)*drlmi
          esl_diff(3) = drlmi*drl_diff(3) + drl(3)*drlmi_diff
          esl(3) = drl(3)*drlmi
C
          rrot_diff(1) = -xyzref_diff(1)
          rrot(1) = 0.5*(rl(1, l2)+rl(1, l1)) - xyzref(1)
          rrot_diff(2) = -xyzref_diff(2)
          rrot(2) = 0.5*(rl(2, l2)+rl(2, l1)) - xyzref(2)
          rrot_diff(3) = -xyzref_diff(3)
          rrot(3) = 0.5*(rl(3, l2)+rl(3, l1)) - xyzref(3)
C
C-------- go over freestream velocity and rotation components
          DO ii1=1,3
            wrot_diff(ii1) = 0.D0
          ENDDO
          CALL CROSS_D(rrot, rrot_diff, wrot, wrot_diff, vrot, vrot_diff
     +                )
C
          temp0 = (vinf(1)+vrot(1))/betm
          veff_diff(1) = (vinf_diff(1)+vrot_diff(1)-temp0*betm_diff)/
     +      betm
          veff(1) = temp0
          veff_diff(2) = vinf_diff(2) + vrot_diff(2)
          veff(2) = vinf(2) + vrot(2)
          veff_diff(3) = vinf_diff(3) + vrot_diff(3)
          veff(3) = vinf(3) + vrot(3)
C
C-------- set VEFF sensitivities to freestream,rotation components
          DO k=1,3
            veff_u(1, k) = 0.
            veff_u(2, k) = 0.
            veff_u(3, k) = 0.
            veff_u(k, k) = 1.0
          ENDDO
C
          DO k=4,6
            wrot_u(1) = 0.
            wrot_u(2) = 0.
            wrot_u(3) = 0.
            wrot_u(k-3) = 1.0
            CALL CROSS(rrot, wrot_u, vrot_u)
C
            veff_u(1, k) = vrot_u(1)
            veff_u(2, k) = vrot_u(2)
            veff_u(3, k) = vrot_u(3)
          ENDDO
C
C-------- U.es
          us_diff = esl(1)*veff_diff(1) + veff(1)*esl_diff(1) + esl(2)*
     +      veff_diff(2) + veff(2)*esl_diff(2) + esl(3)*veff_diff(3) + 
     +      veff(3)*esl_diff(3)
          us = veff(1)*esl(1) + veff(2)*esl(2) + veff(3)*esl(3)
C
C
C-------- velocity projected on normal plane = U - (U.es) es
          DO k=1,3
            un_diff = veff_diff(k) - esl(k)*us_diff - us*esl_diff(k)
            un = veff(k) - us*esl(k)
            fb_diff(k) = src(l)*un_diff
            fb(k) = un*src(l)
C
            DO iu=1,6
              un_u = veff_u(k, iu) - (veff_u(1, iu)*esl(1)+veff_u(2, iu)
     +          *esl(2)+veff_u(3, iu)*esl(3))*esl(k)
              fb_u(k, iu) = un*src_u(l, iu) + un_u*src(l)
            ENDDO
C
            dcpb(k, l) = fb(k)*2.0*dinv*drlmi
          ENDDO
C
          CALL CROSS_D(rrot, rrot_diff, fb, fb_diff, mb, mb_diff)
          DO iu=1,6
            CALL CROSS(rrot, fb_u(1, iu), mb_u(1, iu))
          ENDDO
C
          temp0 = (fb(1)*cosa+fb(3)*sina)/sref
          cdbdy_diff(ib) = cdbdy_diff(ib) + 2.0*(cosa*fb_diff(1)+fb(1)*
     +      cosa_diff+sina*fb_diff(3)+fb(3)*sina_diff-temp0*sref_diff)/
     +      sref
          cdbdy(ib) = cdbdy(ib) + 2.0*temp0
          temp0 = (fb(3)*cosa-fb(1)*sina)/sref
          clbdy_diff(ib) = clbdy_diff(ib) + 2.0*(cosa*fb_diff(3)+fb(3)*
     +      cosa_diff-sina*fb_diff(1)-fb(1)*sina_diff-temp0*sref_diff)/
     +      sref
          clbdy(ib) = clbdy(ib) + 2.0*temp0
C
          temp0 = fb(1)/sref
          cxbdy_diff(ib) = cxbdy_diff(ib) + 2.0*(fb_diff(1)-temp0*
     +      sref_diff)/sref
          cxbdy(ib) = cxbdy(ib) + 2.0*temp0
          temp0 = fb(2)/sref
          cybdy_diff(ib) = cybdy_diff(ib) + 2.0*(fb_diff(2)-temp0*
     +      sref_diff)/sref
          cybdy(ib) = cybdy(ib) + 2.0*temp0
          temp0 = fb(3)/sref
          czbdy_diff(ib) = czbdy_diff(ib) + 2.0*(fb_diff(3)-temp0*
     +      sref_diff)/sref
          czbdy(ib) = czbdy(ib) + 2.0*temp0
C
          temp0 = mb(1)/(bref*sref)
          crbdy_diff(ib) = crbdy_diff(ib) + 2.0*(mb_diff(1)-temp0*bref*
     +      sref_diff)/(bref*sref)
          crbdy(ib) = crbdy(ib) + 2.0*temp0
          temp0 = mb(2)/(cref*sref)
          cmbdy_diff(ib) = cmbdy_diff(ib) + 2.0*(mb_diff(2)-temp0*cref*
     +      sref_diff)/(cref*sref)
          cmbdy(ib) = cmbdy(ib) + 2.0*temp0
          temp0 = mb(3)/(bref*sref)
          cnbdy_diff(ib) = cnbdy_diff(ib) + 2.0*(mb_diff(3)-temp0*bref*
     +      sref_diff)/(bref*sref)
          cnbdy(ib) = cnbdy(ib) + 2.0*temp0
C
          DO iu=1,6
            cdbdy_u(iu) = cdbdy_u(iu) + (fb_u(1, iu)*cosa+fb_u(3, iu)*
     +        sina)*2.0/sref
            clbdy_u(iu) = clbdy_u(iu) + (-(fb_u(1, iu)*sina)+fb_u(3, iu)
     +        *cosa)*2.0/sref
C
            cxbdy_u(iu) = cxbdy_u(iu) + fb_u(1, iu)*2.0/sref
            cybdy_u(iu) = cybdy_u(iu) + fb_u(2, iu)*2.0/sref
            czbdy_u(iu) = czbdy_u(iu) + fb_u(3, iu)*2.0/sref
C
            crbdy_u(iu) = crbdy_u(iu) + mb_u(1, iu)*2.0/sref/bref
            cmbdy_u(iu) = cmbdy_u(iu) + mb_u(2, iu)*2.0/sref/cref
            cnbdy_u(iu) = cnbdy_u(iu) + mb_u(3, iu)*2.0/sref/bref
          ENDDO
        ENDDO
C
C---- add body forces and sensitivities to totals
        cdtot_diff = cdtot_diff + cdbdy_diff(ib)
        cdtot = cdtot + cdbdy(ib)
        cltot_diff = cltot_diff + clbdy_diff(ib)
        cltot = cltot + clbdy(ib)
C
        cxtot_diff = cxtot_diff + cxbdy_diff(ib)
        cxtot = cxtot + cxbdy(ib)
        cytot_diff = cytot_diff + cybdy_diff(ib)
        cytot = cytot + cybdy(ib)
        cztot_diff = cztot_diff + czbdy_diff(ib)
        cztot = cztot + czbdy(ib)
C
        crtot_diff = crtot_diff + crbdy_diff(ib)
        crtot = crtot + crbdy(ib)
        cmtot_diff = cmtot_diff + cmbdy_diff(ib)
        cmtot = cmtot + cmbdy(ib)
        cntot_diff = cntot_diff + cnbdy_diff(ib)
        cntot = cntot + cnbdy(ib)
C
        DO iu=1,6
          cdtot_u(iu) = cdtot_u(iu) + cdbdy_u(iu)
          cltot_u(iu) = cltot_u(iu) + clbdy_u(iu)
C
          cxtot_u(iu) = cxtot_u(iu) + cxbdy_u(iu)
Cccc      CXTOT_U(IU) = CYTOT_U(IU) + CYBDY_U(IU)   <<< BUG  5 Dec 10  MD
          cytot_u(iu) = cytot_u(iu) + cybdy_u(iu)
          cztot_u(iu) = cztot_u(iu) + czbdy_u(iu)
C
          crtot_u(iu) = crtot_u(iu) + crbdy_u(iu)
          cmtot_u(iu) = cmtot_u(iu) + cmbdy_u(iu)
          cntot_u(iu) = cntot_u(iu) + cnbdy_u(iu)
        ENDDO
      ENDDO
C
      RETURN
      END

C  Differentiation of vinfab in forward (tangent) mode (with options i4 dr8 r8):
C   variations   of useful results: vinf
C   with respect to varying inputs: alfa beta
C BDFORC
C
C
C
      SUBROUTINE VINFAB_D()
      INCLUDE 'AVL.INC'
      INCLUDE 'AVL_ad_seeds.inc'
      REAL sina
      REAL sina_diff
      INTRINSIC SIN
      REAL cosa
      REAL cosa_diff
      INTRINSIC COS
      REAL sinb
      REAL sinb_diff
      REAL cosb
      REAL cosb_diff
      INTEGER ii1
C
      sina_diff = COS(alfa)*alfa_diff
      sina = SIN(alfa)
      cosa_diff = -(SIN(alfa)*alfa_diff)
      cosa = COS(alfa)
      sinb_diff = COS(beta)*beta_diff
      sinb = SIN(beta)
      cosb_diff = -(SIN(beta)*beta_diff)
      cosb = COS(beta)
C
      DO ii1=1,3
        vinf_diff(ii1) = 0.D0
      ENDDO
      vinf_diff(1) = cosb*cosa_diff + cosa*cosb_diff
      vinf(1) = cosa*cosb
      vinf_diff(2) = -sinb_diff
      vinf(2) = -sinb
      vinf_diff(3) = cosb*sina_diff + sina*cosb_diff
      vinf(3) = sina*cosb
C
      vinf_a(1) = -(sina*cosb)
      vinf_a(2) = 0.
      vinf_a(3) = cosa*cosb
C
      vinf_b(1) = -(cosa*sinb)
      vinf_b(2) = -cosb
      vinf_b(3) = -(sina*sinb)
C
      RETURN
      END
