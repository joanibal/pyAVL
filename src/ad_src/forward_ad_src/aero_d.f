     +        r_diff(2)-r(3)*dcfy_d_diff-dcfy_d*r_diff(3)-temp*cr_diff)/
     +        cr
            cmx_d(n) = cmx_d(n) + temp
            temp = (dcfx_d*r(3)-dcfz_d*r(1))/cr
