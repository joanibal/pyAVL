C-------- control derivatives
          DO n=1,ncontrol
            temp = fgam_d(1, n)/sr
            dcfx_d_diff = (fgam_d_diff(1, n)-temp*sr_diff)/sr
            dcfx_d = temp
            temp = fgam_d(2, n)/sr
            dcfy_d_diff = (fgam_d_diff(2, n)-temp*sr_diff)/sr
            dcfy_d = temp
            temp = fgam_d(3, n)/sr
            dcfz_d_diff = (fgam_d_diff(3, n)-temp*sr_diff)/sr
            dcfz_d = temp
